                wncxspec = "{:.4f}".format(-wcx)
                wcyspec = "{:.4f}".format(wcy)

                wirey = wbase
                wyspec = "{:.4f}".format(wirey)

                print('* ' + layer[0] + ' to ' + layer_above[0], file=ofile)
                print('D k_sidewall_' + layer[0] + '_left ' + kvalue + ' ' + kabove + ' ' + wncxspec + ' ' + wyspec + ' ' + wncxspec + ' ' + wcyspec + ' -', file=ofile)
