                    hspacen = "{:.4f}".format(-halfspace)
                    ybase = "{:.4f}".format(layer_below[2])
                    kbelow = "{:.2f}".format(layer_below[5])

                    print('* Metal ' + metal + ' left', file=ofile)

                    print('C ' + metal + '_top  ' + kabove + '  ' + hspacen + '  ' + ybase + ' +', file=ofile)
